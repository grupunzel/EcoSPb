async def photo_detection(photo_bytes: bytes) -> str:
    if not YOLO_AVAILABLE:
        return "⚠️ Распознавание временно недоступно. Пожалуйста, опишите предмет текстом."

    try:
        image = Image.open(io.BytesIO(photo_bytes))
        image_np = np.array(image.convert("RGB"))

        results = yolo_model(image_np)
        detections = results[0].boxes

        if len(detections) == 0:
            return "🤔 Ничего не распознано. Попробуйте сфотографировать предмет крупнее."

        top_detection = detections[0]
        class_id = int(top_detection.cls)
        class_name = yolo_model.names[class_id]
        confidence = float(top_detection.conf)

        if confidence < 0.5:
            return "🤔 Изображение нечёткое или объект не распознан. Попробуйте ещё раз."

        tip = await get_ai_answer(class_name)
        return f"🔍 **Я вижу:** {class_name} (уверенность {confidence:.0%})\n\n{tip}"

    except Exception as e:
        return "⚠️ Не удалось обработать фото. Пожалуйста, опишите предмет текстом."